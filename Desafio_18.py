import math
an = float(input('Digite o ângulo que você deseja:'))
print(f'O ângulo de {an:.1f} tem o SENO de {math.sin(math.radians(an)):.1f}')
print (f'O ângulo de {an:.1f} tem o COSSENO de {math.cos(math.radians(an)):.1f}')
print (f'O ângulo de {an:.1f} tem a TANGENTE de {math.tan(math.radians(an)):.1f}')